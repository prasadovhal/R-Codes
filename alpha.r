quadrant <- function(alpha)
{
   if (alpha < 90)
   {
    print("alpha is in 1st quadrant")
   }
  else
   if (alpha <180 )
   {
    print("alpha is in 2nd quadrant")
   }
  else
   if (alpha < 270)
   {
    print("alpha is in 3rd quadrant")
   }
  else
   if (alpha < 360)
   {
    print("alpha is in 4th quadrant")
   }
}
